/* 
Assignment 3 – Practical Question 1
How arithmetic operators and concatenation work in the browser

Explanation:
Arithmetic operators are used to perform mathematical operations on numbers. Common operators include:
+ → addition
- → subtraction
* → multiplication
/ → division
% → modulus (remainder)

Concatenation is the process of joining two values together, usually strings, using the + operator.

In the browser (or JavaScript), arithmetic and concatenation can be done directly in scripts or in the console.
*/

// Program 1: Addition
console.log(5 + 3); // Output: 8

// Program 2: Subtraction
console.log(10 - 4); // Output: 6

// Program 3: Multiplication
console.log(6 * 7); // Output: 42

// Program 4: Division
console.log(20 / 4); // Output: 5

// Program 5: Modulus
console.log(10 % 3); // Output: 1 (remainder)

// Program 6: Concatenation with strings
console.log("Hello " + "World"); // Output: Hello World

// Program 7: Concatenation numbers and strings
console.log("Result: " + (5 + 3)); // Output: Result: 8